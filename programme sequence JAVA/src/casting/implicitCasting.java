package casting;

public class implicitCasting {

	//it is a primitive casting type
	
	//casting=convering information from one type to another type is called casting
	
	//implicit casting is converinng the lower datatype information in to the higher datatype is called implicit casting
	
	
	public static void main (String[]args) {
		
		
		int a=5;  //lower datatype
		
		System.out.println(a);
		
		double x=a; //higher datatype
		System.out.println(x);
		
		
	}
	
	
}
