package casting;

public class explicitCasting {
	
	
	//it is primitive type casting ,it is used to convert higher datatype information in to lower datatype
	
	
	
	//disadvantage is possibility to loss data
	
	public static void main(String[]args) {
		
	int a=89506589;  //higher datatype
	
	byte b=(byte) a;
	
	System.out.println(b);

	
	double d=45656819;
	
	float e=(float) d;
	
	System.out.println(d);

		
		
	}

}
