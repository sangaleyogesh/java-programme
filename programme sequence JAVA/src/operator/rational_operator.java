package operator;

public class rational_operator{
	
	public static void main(String[]args) {
		
		//rational operator
		// a b a>b; a<b; a>=b; a<=b; a!=b;
		int	a=20; 
		
		int b=30;
		
		System.out.println(a<b);
		
		System.out.println(a>b);
		
		System.out.println(a==b);
		
		System.out.println(a>=b);
		
		System.out.println(a<=b);
		
		System.out.println(a!=b);

		System.out.println(b!=a);
	}

	
	
	
	
}