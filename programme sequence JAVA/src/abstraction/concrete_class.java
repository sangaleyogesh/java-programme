package abstraction;

public class concrete_class extends incomplete_or_abstract_class {

	@Override
	public void m2() { 
		
		System.out.println("m2 method from incomplete class");
		
	}

	@Override
	public void m3() {
		System.out.println("m3 method from incomplete class");
		
	}

	
	//abstract or incomplete class can be implemented in concrete class by using extend keyword
	
	
}
