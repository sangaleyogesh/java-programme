package loops;

public class for_while_dowhile_combine{
	
	public static void main(String[]args) {
		
		//1.for loop
		
		for(int i=1; i<=10; i++) {
			
			System.out.println(i);}
			
		//2.while loop
		
		int i1=1;
		
		while(i1<=10) {
			
			System.out.println(i1);
		
			i1++;
		}
		//3.do while
		
		int j=1;
		
		do {
			
			System.out.println(j);
		j++;
		}
		
		while (j<=10) ;
		
	}
}
		
	
	
	
	
	
	
