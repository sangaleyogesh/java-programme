package loops;

public class for_while_dowhile1{

public static void main(String[]args){

//printing 1to5 numbers using for loop

for(int i=1; i<=5;i++){

System.out.println(i);

}

//printing1 to 5 numbers using while loop

int j=1;
while(j<=5){
System.out.println(j);
j++;
}

// printing 1to5 numbers using do_while loop

int k=1;

do{

System.out.println(k);
k++;
}

while(k<=5);
}



}