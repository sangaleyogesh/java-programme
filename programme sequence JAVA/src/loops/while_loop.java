package loops;

public class while_loop{
	
	public static void main(String[]args) {
		
	// printing 1 to 10 numbers
	// syntax= variable initialisation
		
	// condition
	// increment or decrement
		
		int i=1;
		
		while(i<=10){
			
			
			System.out.println(i);
			
			i++;
		}
		
		
		
	}
	
	
	
	
	
}